const Joi = require('joi');

const createAccountSchema = Joi.object({
  name:          Joi.string().max(255).required(),
  accountType:   Joi.string().max(100).optional().allow(null, ''),
  industry:      Joi.string().max(100).optional().allow(null, ''),
  website:       Joi.string().uri().max(255).optional().allow(null, ''),
  phone:         Joi.string().max(50).optional().allow(null, ''),
  email:         Joi.string().email().max(255).optional().allow(null, ''),
  annualRevenue: Joi.number().min(0).optional().allow(null),
  employees:     Joi.number().integer().min(0).optional().allow(null),
  billingStreet:  Joi.string().max(255).optional().allow(null, ''),
  billingCity:    Joi.string().max(100).optional().allow(null, ''),
  billingState:   Joi.string().max(100).optional().allow(null, ''),
  billingCountry: Joi.string().max(100).optional().allow(null, ''),
  billingZip:     Joi.string().max(20).optional().allow(null, ''),
  description:   Joi.string().optional().allow(null, ''),
  status:        Joi.string().valid('active', 'inactive').default('active'),
  ownerId:       Joi.string().uuid().optional().allow(null),

  // New CRM B2B Fields
  category:             Joi.string().max(100).optional().allow(null, ''),
  organizationProfile:  Joi.string().max(255).optional().allow(null, ''),
  subIndustry:          Joi.string().max(100).optional().allow(null, ''),
  linkedinPage:         Joi.string().uri().max(255).optional().allow(null, ''),
  yearEstablished:      Joi.number().integer().min(1800).max(new Date().getFullYear() + 1).optional().allow(null),
  employeeSize:         Joi.string().max(100).optional().allow(null, ''),
  annualRevenueRange:   Joi.string().max(100).optional().allow(null, ''),
  marketSegment:        Joi.string().max(100).optional().allow(null, ''),
  officesCount:         Joi.number().integer().min(0).optional().allow(null),
  globalPresence:       Joi.boolean().optional().allow(null),
  countriesOperation:   Joi.array().items(Joi.string()).optional().allow(null),
  parentCompanyId:      Joi.string().uuid().optional().allow(null, ''),
  gstNumber:            Joi.string().max(50).optional().allow(null, ''),
  panNumber:            Joi.string().max(50).optional().allow(null, ''),
  cinNumber:            Joi.string().max(50).optional().allow(null, ''),
  paymentTerms:         Joi.string().max(100).optional().allow(null, ''),
  creditLimit:          Joi.number().min(0).optional().allow(null),
  taxExemptionStatus:   Joi.boolean().optional().allow(null),
  billingStreet2:       Joi.string().max(255).optional().allow(null, ''),
  shippingLocation:     Joi.string().max(255).optional().allow(null, ''),
  shippingStreet:       Joi.string().max(255).optional().allow(null, ''),
  shippingCity:         Joi.string().max(100).optional().allow(null, ''),
  shippingCountry:      Joi.string().max(100).optional().allow(null, ''),
  shippingPhone:        Joi.string().max(50).optional().allow(null, ''),
  primaryContactName:   Joi.string().max(255).optional().allow(null, ''),
  primaryContactDesignation: Joi.string().max(255).optional().allow(null, ''),
  primaryContactDepartment:   Joi.string().max(255).optional().allow(null, ''),
  primaryContactEmail:  Joi.string().email().max(255).optional().allow(null, ''),
  primaryContactMobile: Joi.string().max(50).optional().allow(null, ''),
  primaryContactPhone:  Joi.string().max(50).optional().allow(null, ''),
  primaryContactLinkedin: Joi.string().uri().max(255).optional().allow(null, ''),
  cloudProvider:        Joi.string().max(255).optional().allow(null, ''),
  dataCentreProvider:   Joi.string().max(255).optional().allow(null, ''),
  systemIntegrator:     Joi.string().max(255).optional().allow(null, ''),
  securityPartner:      Joi.string().max(255).optional().allow(null, ''),
  techPlatforms:        Joi.string().optional().allow(null, ''),
  digitalInitiatives:   Joi.string().optional().allow(null, ''),
  existingContracts:    Joi.string().optional().allow(null, ''),
  renewalDates:         Joi.date().iso().optional().allow(null),
  leadSource:           Joi.string().max(100).optional().allow(null, ''),
  accountSource:        Joi.string().max(100).optional().allow(null, ''),
  referralPartner:      Joi.string().max(255).optional().allow(null, ''),
  accountPriority:      Joi.string().max(100).optional().allow(null, ''),
  accountTier:          Joi.string().max(100).optional().allow(null, ''),
  strategicAccount:     Joi.boolean().optional().allow(null),
  estimatedRevenue:     Joi.number().min(0).optional().allow(null),
  currentSpend:         Joi.number().min(0).optional().allow(null),
  competitorInfo:       Joi.string().optional().allow(null, ''),
  keyChallenges:        Joi.string().optional().allow(null, ''),
  nextAction:           Joi.string().max(255).optional().allow(null, ''),
  nextFollowUpDate:     Joi.date().iso().optional().allow(null),
  existingCustomer:     Joi.boolean().optional().allow(null),
  currentProducts:      Joi.string().optional().allow(null, ''),
  previousOrders:       Joi.string().optional().allow(null, ''),
  lastPurchaseDate:     Joi.date().iso().optional().allow(null),
  activeOpportunities:  Joi.number().integer().min(0).optional().allow(null),
  expectedDealSize:     Joi.number().min(0).optional().allow(null),
  expectedClosureDate:  Joi.date().iso().optional().allow(null),
  probabilityClosure:   Joi.number().integer().min(0).max(100).optional().allow(null),
  territory:            Joi.string().max(100).optional().allow(null, ''),
  region:               Joi.string().max(100).optional().allow(null, ''),
  businessUnit:         Joi.string().max(100).optional().allow(null, ''),
  dataQualityScore:     Joi.number().integer().min(0).max(100).optional().allow(null),
});

const updateAccountSchema = createAccountSchema.fork(
  ['name'],
  (schema) => schema.optional()
);

const listAccountsSchema = Joi.object({
  page:     Joi.number().integer().min(1).default(1),
  limit:    Joi.number().integer().min(1).max(100).default(20),
  search:   Joi.string().max(255).optional().allow(''),
  status:   Joi.string().valid('active', 'inactive').optional(),
  industry: Joi.string().max(100).optional().allow(''),
  sortBy:   Joi.string().valid('name', 'createdAt', 'annualRevenue', 'estimatedRevenue').default('createdAt'),
  sortDir:  Joi.string().valid('ASC', 'DESC').default('DESC'),
});

module.exports = { createAccountSchema, updateAccountSchema, listAccountsSchema };
